    public Cliente tbClienteLinhaSelecionada(JTable tb) {
        Cliente cliente = null;
        if (tb.getSelectedRow() != -1) {
            cliente = new Cliente();
            cliente.setCodigo_cliente(clientes.get(tb.getSelectedRow()).getCodigo_cliente());
        }
        return cliente;
    }

